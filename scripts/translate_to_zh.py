def main():
    raw = json.loads(SRC.read_text(encoding="utf-8"))
    sections = {"tech": {"name": "科技产业", "articles": []},
                "americas": {"name": "美洲时政", "articles": []}}

    for art in raw["articles"]:
        sec = art["section"]
        if sec not in sections:
            continue
        title_zh, summary_zh = TRANSLATIONS[art["id"]]
        ordered = OrderedDict()
        ordered["id"] = art["id"]
        ordered["title_zh"] = title_zh
        ordered["summary_zh"] = summary_zh
        ordered["title_en"] = art["title_en"]
        ordered["source"] = art["source"]
        ordered["url"] = art["url"]
        ordered["published"] = art["published_display"]
        sections[sec]["articles"].append(ordered)

    out = OrderedDict()
    out["date"] = "2026-07-06"
    out["updated_at"] = "2026-07-06 11:30 北京时间"
    out["total"] = sum(len(s["articles"]) for s in sections.values())
    out_sections = OrderedDict()
    for key in ("tech", "americas"):
        s = sections[key]
        out_sections[key] = OrderedDict([
            ("name", s["name"]),
            ("count", len(s["articles"])),
            ("articles", s["articles"]),
        ])
    out["sections"] = out_sections

    DST.parent.mkdir(parents=True, exist_ok=True)
    DST.write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"写入完成: {DST}")
    print(f"总数: {out['total']}")
    for k, v in out_sections.items():
        print(f"  {k} ({v['name']}): {v['count']}")

    # 长度校验
    print("\n摘要长度校验 (中文字符):")
    out_obj = json.loads(DST.read_text(encoding="utf-8"))
    bad = 0
    for sec_name, sec in out_obj["sections"].items():
        for art in sec["articles"]:
            zh_len = sum(1 for c in art["summary_zh"] if '\u4e00' <= c <= '\u9fff')
            if not (250 <= zh_len <= 350):
                print(f"  ⚠️ {art['id']}: {zh_len}字")
                bad += 1
    if bad == 0:
        print("  所有摘要均在 250-350 字范围内 ✓")
    else:
        print(f"  共 {bad} 条超出范围")


if __name__ == "__main__":
    main()
