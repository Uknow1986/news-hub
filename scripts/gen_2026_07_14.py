    missing, excluded = [], []
    # 明确排除的离题 id（足球/赛车/非美洲地缘/生活花絮）
    EXCLUDE = {
        "05a7bed5fce3", "9ebb805274df", "a9539aa5bd07", "4533aa23881c",
        "3166e81204b8", "feb78809a7de", "3b4c6d48357a",
    }
    for a in articles:
        if a['id'] in EXCLUDE:
            excluded.append(a['title_en']); continue
        t = TRANSLATIONS.get(a['id'])
        if not t:
            missing.append(a['title_en']); continue
        title_zh, summary_zh = t
        summary_zh = summary_zh + TAILS.get(a['id'], '')
        sections[a['section']].append({
            'id': a['id'],
            'title_zh': title_zh,
            'summary_zh': summary_zh,
            'title_en': a['title_en'],
            'source': a['source'],
            'url': a['url'],
            'published': a.get('published_display', ''),
        })

    beijing = timezone(timedelta(hours=8))
    now = datetime.now(beijing)
    daily = {
        'date': now.strftime('%Y-%m-%d'),
        'updated_at': now.strftime('%Y-%m-%d %H:%M 北京时间'),
        'total': len(sections['tech']) + len(sections['americas']),
        'sections': {},
    }
    for sk, sn in SECTION_NAMES.items():
        sa = sections[sk]
        daily['sections'][sk] = {'name': sn, 'count': len(sa), 'articles': sa}

    out = DATA_DIR / f"{now.strftime('%Y-%m-%d')}.json"
    out.write_text(json.dumps(daily, ensure_ascii=False, indent=2), encoding='utf-8')

    idx = json.loads((DATA_DIR / 'index.json').read_text(encoding='utf-8'))
    today = now.strftime('%Y-%m-%d')
    if today not in idx:
        idx.insert(0, today)
    (DATA_DIR / 'index.json').write_text(json.dumps(idx, ensure_ascii=False, indent=2), encoding='utf-8')

    print('WROTE', out)
    print('tech:', len(sections['tech']), '| americas:', len(sections['americas']), '| total:', daily['total'])
    print('excluded:', len(excluded), '| missing:', len(missing))
    for m in missing:
        print('  MISSING:', m[:80])
