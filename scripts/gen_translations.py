def main():
    with open(SRC, "r", encoding="utf-8") as f:
        data = json.load(f)

    sections_map = {
        "tech": {"name": "科技产业", "articles": []},
        "americas": {"name": "美洲时政", "articles": []},
    }

    missing = []
    for art in data["articles"]:
        aid = art["id"]
        sec = art["section"]
        if sec not in sections_map:
            continue
        if aid not in TRANSLATIONS:
            missing.append(aid)
            continue
        title_zh, summary_zh = TRANSLATIONS[aid]
        sections_map[sec]["articles"].append({
            "id": aid,
            "title_zh": title_zh,
            "summary_zh": summary_zh,
            "title_en": art["title_en"],
            "source": art["source"],
            "url": art["url"],
            "published": art["published_display"],
        })

    if missing:
        print(f"警告: {len(missing)} 条文章缺少翻译: {missing}")
        return

    beijing_tz = timezone(timedelta(hours=8))
    now_beijing = datetime.now(beijing_tz).strftime("%Y-%m-%d %H:%M 北京时间")

    output = {
        "date": "2026-07-01",
        "updated_at": now_beijing,
        "total": sum(len(s["articles"]) for s in sections_map.values()),
        "sections": {},
    }

    for sec_key, sec_data in sections_map.items():
        output["sections"][sec_key] = {
            "name": sec_data["name"],
            "count": len(sec_data["articles"]),
            "articles": sec_data["articles"],
        }

    DST.parent.mkdir(parents=True, exist_ok=True)
    with open(DST, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    # 验证摘要字数
    print(f"已写入 {output['total']} 条文章到 {DST}")
    for sec_key, sec_data in output["sections"].items():
        print(f"  {sec_data['name']}: {sec_data['count']} 条")
        for art in sec_data["articles"]:
            n = len(art["summary_zh"])
            flag = ""
            if n < 250 or n > 350:
                flag = f"  <-- 字数异常 ({n})"
            print(f"    {art['id']}: {n}字{flag}")


if __name__ == "__main__":
    main()
