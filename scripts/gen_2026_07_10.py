def main():
    with open(SRC, encoding="utf-8") as f:
        data = json.load(f)
    articles = data["articles"]

    sections = {"tech": [], "americas": []}
    translated = 0
    missing = []
    short_long = []
    for a in articles:
        tid = a["id"]
        if tid not in TRANSLATIONS:
            missing.append(a["title_en"])
            continue
        title_zh, summary_zh = TRANSLATIONS[tid]
        a["title_zh"] = title_zh
        a["summary_zh"] = summary_zh
        sections[a["section"]].append(a)
        translated += 1
        # 字数校验
        n = len(summary_zh)
        if not (250 <= n <= 350):
            short_long.append((tid, n, a["section"]))

    print(f"翻译完成: {translated}/{len(articles)} 条")
    if missing:
        print(f"缺翻译: {len(missing)}")
        for m in missing:
            print("  缺:", m[:80])
    if short_long:
        print(f"字数异常 (需 250-350): {len(short_long)}")
        for tid, n, sec in short_long:
            print(f"  {tid} [{sec}] 字数={n}")

    now = datetime.now(BEIJING)
    daily = {
        "date": today,
        "updated_at": now.strftime("%Y-%m-%d %H:%M 北京时间"),
        "total": translated,
        "sections": {},
    }
    for sk, sn in SECTION_NAMES.items():
        sa = sections[sk]
        daily["sections"][sk] = {
            "name": sn,
            "count": len(sa),
            "articles": [
                {
                    "id": a["id"],
                    "title_zh": a["title_zh"],
                    "summary_zh": a["summary_zh"],
                    "title_en": a["title_en"],
                    "source": a["source"],
                    "url": a["url"],
                    "published": a.get("published_display", ""),
                }
                for a in sa
            ],
        }

    with open(DST, "w", encoding="utf-8") as f:
        json.dump(daily, f, ensure_ascii=False, indent=2)

    # 更新 index.json
    with open("/workspace/news-hub/data/index.json", encoding="utf-8") as f:
        dates = json.load(f)
    if today not in dates:
        dates.insert(0, today)
    with open("/workspace/news-hub/data/index.json", "w", encoding="utf-8") as f:
        json.dump(dates, f, ensure_ascii=False, indent=2)

    print(f"\n已保存: {DST}")
    for sk, sv in daily["sections"].items():
        print(f"  {sv['name']}: {sv['count']} 条")
    print(f"历史归档: {dates}")


if __name__ == "__main__":
    main()
